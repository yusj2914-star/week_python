# max()
# 요소 중에서 가장 큰 값을 반환
print(max([1, 2, 3]))
print(max("python"))
# print(max([123, "python", "You"]))
# 문자열 비교는 가장 앞에 있는 요소를 가져옴(1, p, Y)
print(max(['123', "python", "You"]))


# min()
# 요소 중에서 가장 작은 값을 반환
print(min([1, 2, 3]))
print(min("python"))
print(min(['123', "python", "You"]))


# ord()
# 문자를 유니코드 값으로 변경
print(ord('a'))
print(ord('가'))

